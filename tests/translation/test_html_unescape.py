"""Tests for HTML entity unescaping in tool call arguments.

Verifies that HTML entities (&amp; &lt; &gt; &quot; &#39;) generated by
Grok in tool call arguments are converted back to real characters before
reaching Claude Code. This fixes the "Bash tool chaining" bug (issue #61)
where Grok returns &&amp;&amp; instead of && in shell commands.

Coverage:
1. unescape_html_entities() function directly
2. _unescape_args() recursive handling
3. Non-streaming reverse translation -- LEGACY (CC) and PRIMARY (Responses API)
4. Streaming translation -- LEGACY (CC) and PRIMARY (Responses adapter)
5. Integration: full tool_use round-trip with HTML-encoded Bash command
"""

from __future__ import annotations

import asyncio
import json
from typing import Any, AsyncIterator

import pytest

from translation.reverse import (
    unescape_html_entities,
    _unescape_args,
    openai_to_anthropic,
)
from translation.responses_reverse import responses_to_anthropic
from translation.streaming import (
    translate_sse_event,
    OpenAIToAnthropicStreamAdapter,
    _tool_events,
)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


async def _lines_to_async_iter(lines: list[str]) -> AsyncIterator[str]:
    for line in lines:
        yield line


async def _collect_events(
    adapter: OpenAIToAnthropicStreamAdapter,
) -> list[dict[str, Any]]:
    events: list[dict[str, Any]] = []
    async for event in adapter:
        events.append(event)
    return events


# ---------------------------------------------------------------------------
# unescape_html_entities() unit tests
# ---------------------------------------------------------------------------


class TestUnescapeHtmlEntities:
    """Direct tests for the unescape_html_entities function."""

    def test_amp_entity(self) -> None:
        assert unescape_html_entities("a &amp; b") == "a & b"

    def test_lt_entity(self) -> None:
        assert unescape_html_entities("x &lt; y") == "x < y"

    def test_gt_entity(self) -> None:
        assert unescape_html_entities("x &gt; y") == "x > y"

    def test_quot_entity(self) -> None:
        assert unescape_html_entities("say &quot;hello&quot;") == 'say "hello"'

    def test_apos_entity(self) -> None:
        assert unescape_html_entities("it&#39;s") == "it's"

    def test_numeric_entity(self) -> None:
        assert unescape_html_entities("&#60;tag&#62;") == "<tag>"

    def test_hex_entity(self) -> None:
        assert unescape_html_entities("&#x3C;html&#x3E;") == "<html>"

    def test_multiple_entities(self) -> None:
        text = "cd /tmp &amp;&amp; ls -la &gt; output.txt"
        expected = "cd /tmp && ls -la > output.txt"
        assert unescape_html_entities(text) == expected

    def test_fast_path_no_ampersand(self) -> None:
        """Text without & returns immediately (fast path)."""
        text = "echo hello world"
        assert unescape_html_entities(text) is text

    def test_empty_string(self) -> None:
        assert unescape_html_entities("") == ""

    def test_ampersand_not_entity(self) -> None:
        """Bare ampersand that is not an entity passes through."""
        text = "foo & bar"
        assert unescape_html_entities(text) == "foo & bar"

    def test_already_clean(self) -> None:
        """Text with real characters (no entities) is unchanged."""
        text = "echo hello && echo world"
        # Contains &, so no fast path, but no entities to decode.
        assert unescape_html_entities(text) == text


# ---------------------------------------------------------------------------
# _unescape_args() recursive tests
# ---------------------------------------------------------------------------


class TestUnescapeArgs:
    """Tests for recursive HTML entity unescaping in parsed args."""

    def test_string_value(self) -> None:
        assert _unescape_args("a &amp; b") == "a & b"

    def test_dict_values(self) -> None:
        obj = {"command": "ls &amp;&amp; pwd", "flag": "-la"}
        expected = {"command": "ls && pwd", "flag": "-la"}
        assert _unescape_args(obj) == expected

    def test_nested_dict(self) -> None:
        obj = {"outer": {"inner": "x &lt; y"}}
        expected = {"outer": {"inner": "x < y"}}
        assert _unescape_args(obj) == expected

    def test_list_values(self) -> None:
        obj = ["a &amp; b", "c &gt; d"]
        expected = ["a & b", "c > d"]
        assert _unescape_args(obj) == expected

    def test_mixed_nested(self) -> None:
        obj = {
            "commands": ["cd /tmp &amp;&amp; ls", "echo &quot;hi&quot;"],
            "opts": {"redirect": "&gt; out.txt"},
        }
        expected = {
            "commands": ["cd /tmp && ls", 'echo "hi"'],
            "opts": {"redirect": "> out.txt"},
        }
        assert _unescape_args(obj) == expected

    def test_non_string_passthrough(self) -> None:
        """Non-string values (int, float, bool, None) pass through."""
        assert _unescape_args(42) == 42
        assert _unescape_args(3.14) == 3.14
        assert _unescape_args(True) is True
        assert _unescape_args(None) is None

    def test_empty_dict(self) -> None:
        assert _unescape_args({}) == {}

    def test_empty_list(self) -> None:
        assert _unescape_args([]) == []

    def test_dict_keys_not_unescaped(self) -> None:
        """Dict keys are NOT unescaped -- only values."""
        obj = {"a &amp; b": "c &amp; d"}
        result = _unescape_args(obj)
        # Keys pass through as-is; values are unescaped.
        assert "a &amp; b" in result
        assert result["a &amp; b"] == "c & d"


