class TestChatCompletionsStreamingToolArgs:
    """LEGACY: Tool argument deltas through CC streaming translator."""

    def test_tool_delta_entities_unescaped(self) -> None:
        """HTML entities in streaming tool argument deltas are unescaped."""
        tc = {
            "index": 0,
            "function": {"arguments": '{"command": "ls &amp;&amp; pwd"}'},
        }
        events = _tool_events(tc)
        json_deltas = [
            e for e in events
            if e.get("delta", {}).get("type") == "input_json_delta"
        ]
        assert len(json_deltas) == 1
        partial = json_deltas[0]["delta"]["partial_json"]
        assert "&amp;" not in partial
        assert "&&" in partial

    @pytest.mark.asyncio
    async def test_adapter_tool_args_entities_unescaped(self) -> None:
        """Full CC stream adapter unescapes entities in tool arg deltas."""
        chunks = [
            {
                "id": "chatcmpl-ent-stream",
                "object": "chat.completion.chunk",
                "created": 1709000400,
                "model": "grok-4-1-fast-reasoning",
                "choices": [{
                    "index": 0,
                    "delta": {
                        "role": "assistant",
                        "content": None,
                        "tool_calls": [{
                            "index": 0,
                            "id": "call_ent_stream",
                            "type": "function",
                            "function": {"name": "Bash", "arguments": ""},
                        }],
                    },
                    "finish_reason": None,
                }],
            },
            {
                "id": "chatcmpl-ent-stream",
                "object": "chat.completion.chunk",
                "created": 1709000400,
                "model": "grok-4-1-fast-reasoning",
                "choices": [{
                    "index": 0,
                    "delta": {
                        "tool_calls": [{
                            "index": 0,
                            "function": {
                                "arguments": '{"command": "cd /tmp &amp;&amp; ls"}',
                            },
                        }],
                    },
                    "finish_reason": None,
                }],
            },
            {
                "id": "chatcmpl-ent-stream",
                "object": "chat.completion.chunk",
                "created": 1709000400,
                "model": "grok-4-1-fast-reasoning",
                "choices": [{"index": 0, "delta": {}, "finish_reason": "tool_calls"}],
            },
        ]
        lines = [f"data: {json.dumps(c)}" for c in chunks]
        lines.append("data: [DONE]")

        source = _lines_to_async_iter(lines)
        adapter = OpenAIToAnthropicStreamAdapter(source)
        events = await _collect_events(adapter)

        json_deltas = [
            e for e in events
            if e["type"] == "content_block_delta"
            and e.get("delta", {}).get("type") == "input_json_delta"
        ]
        full_json = "".join(d["delta"]["partial_json"] for d in json_deltas)
        parsed = json.loads(full_json)
        assert parsed["command"] == "cd /tmp && ls"


